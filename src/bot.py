async def convert(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∫–æ–º–∞–Ω–¥—É /convert –¥–ª—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏ —Å—É–º–º—ã –∏–∑ –æ–¥–Ω–æ–π –≤–∞–ª—é—Ç—ã –≤ –¥—Ä—É–≥—É—é.

    –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏ –º–æ–≥—É—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —ç—Ç—É –∫–æ–º–∞–Ω–¥—É –¥–ª—è –∑–∞–ø—Ä–æ—Å–∞ –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏ –≤–∞–ª—é—Ç –ø–æ —Ç–µ–∫—É—â–µ–º—É –∫—É—Ä—Å—É.
    –ö–æ–º–∞–Ω–¥–∞ —Ç—Ä–µ–±—É–µ—Ç —É–∫–∞–∑–∞–Ω–∏—è —Å—É–º–º—ã –∏ –ø–∞—Ä—ã –≤–∞–ª—é—Ç –≤ —Ñ–æ—Ä–º–∞—Ç–µ: /convert <—Å—É–º–º–∞> <–∏–∑ –≤–∞–ª—é—Ç—ã> to <–≤ –≤–∞–ª—é—Ç—É>.
    –í —Å–ª—É—á–∞–µ —É—Å–ø–µ—Ö–∞, –±–æ—Ç –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏. –ï—Å–ª–∏ –ø—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä,
    –Ω–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –∫–æ–º–∞–Ω–¥—ã –∏–ª–∏ –ø—Ä–æ–±–ª–µ–º—ã —Å –ø–æ–ª—É—á–µ–Ω–∏–µ–º –∫—É—Ä—Å–∞ –≤–∞–ª—é—Ç), –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø–æ–ª—É—á–∞–µ—Ç —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–µ
    —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ.
    """
    logger.info("–ö–æ–º–∞–Ω–¥–∞ /convert –ø–æ–ª—É—á–µ–Ω–∞")
    args = context.args
    if len(args) != 4 or args[2].lower() != "to":
        await update.message.reply_text(
            "–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Ñ–æ—Ä–º–∞—Ç: /convert <—Å—É–º–º–∞> <–∏–∑ –≤–∞–ª—é—Ç—ã> to <–≤ –≤–∞–ª—é—Ç—É>."
        )
        return

    try:
        amount = Decimal(args[0])
        from_currency = args[1].upper()
        to_currency = args[3].upper()
        result = convert_currency(amount, from_currency, to_currency)
        if result is not None:
            await update.message.reply_text(
                f"{amount} {from_currency} = {result} {to_currency}"
            )
        else:
            await update.message.reply_text("–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –∫—É—Ä—Å –≤–∞–ª—é—Ç.")
    except ValueError as e:
        logger.info(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –∫–æ–º–∞–Ω–¥–µ /convert: {e}")
        await update.message.reply_text("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É–∫–∞–∂–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—É—é —Å—É–º–º—É.")


async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç —Ç–µ–∫—Å—Ç–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π.

    –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è —Ä–µ–∞–≥–∏—Ä—É–µ—Ç –Ω–∞ –æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω—ã–µ –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ –≤ —Ç–µ–∫—Å—Ç–µ —Å–æ–æ–±—â–µ–Ω–∏–π. –ï—Å–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å–æ–¥–µ—Ä–∂–∏—Ç
    —Å–ª–æ–≤–æ "–ø—Ä–∏–≤–µ—Ç", —Ñ—É–Ω–∫—Ü–∏—è –æ—Ç–≤–µ—á–∞–µ—Ç –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ–º. –ï—Å–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å–æ–¥–µ—Ä–∂–∏—Ç "–ø–æ–∫–∞", —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–æ—â–∞–µ—Ç—Å—è.
    –í–æ –≤—Å–µ—Ö –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —Å–ª—É—á–∞—è—Ö —Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–µ–¥–ª–∞–≥–∞–µ—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∫–æ–º–∞–Ω–¥—É /help –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å–ø–∏—Å–∫–∞ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –∫–æ–º–∞–Ω–¥.
    """
    logger.info("–í—Ö–æ–¥—è—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–±—Ä–∞–±–æ—Ç–∞–Ω–æ")
    text = update.message.text.lower()
    if "–ø—Ä–∏–≤–µ—Ç" in text:
        await update.message.reply_text("–ü—Ä–∏–≤–µ—Ç!")
    elif "–ø–æ–∫–∞" in text:
        await update.message.reply_text("–î–æ —Å–≤–∏–¥–∞–Ω–∏—è!")
    else:
        await update.message.reply_text(
            "–ù–µ —É–≤–µ—Ä–µ–Ω, –∫–∞–∫ –Ω–∞ —ç—Ç–æ –æ—Ç–≤–µ—Ç–∏—Ç—å. –ò—Å–ø–æ–ª—å–∑—É–π /help."
        )


